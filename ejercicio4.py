'''
Dados dos números, mostrar la suma resta, resta, división y multiplicación de ambos
suma
resta
división
multiplicación
'''
n1 = input("Escribe el número uno: ")
n1 = int(n1)
n2 = input("Escribe el número dos: ")
n2 = int(n2)
a = n1 + n2
b = n1 - n2
c = n1 / n2
d = n1 * n2
print("La suma de los dos números es: ",a)
print("La diferencia de los dos números es: ",b)
print("El cociente de los dos números es: ",c)
print("El producto de los dos números es: ",d)